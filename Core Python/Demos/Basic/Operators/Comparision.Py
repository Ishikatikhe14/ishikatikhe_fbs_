####Reltional
x= 10
y= 20
a= 'abc'

#1. == #exactly equal to
print(x == y)  # False
print(a == '10')  # False

#2. != #not equal to
print(x != '10')# True

#3. > #greater than
print(x >5)  # False

#4. >= #greater than or equal to
print(x >= 10)  # True

#5. < #less than
print(x < y)  # False

#6. <= #less than or equal to
print(x <= 10)  # True

